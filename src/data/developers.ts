// Helper function to search developers
export const searchDevelopers = (query: string): string[] => {
  if (!query.trim()) return DEVELOPERS;
  const lowerQuery = query.toLowerCase();
  return DEVELOPERS.filter(d => d.toLowerCase().includes(lowerQuery));
};

// Check if a developer name is in the static list
export const isDeveloperInList = (name: string): boolean => {
  return DEVELOPERS.some(d => d.toLowerCase() === name.toLowerCase());
};
